                            <p class="fs-5 mb-4">Although only 220 sq miles (580 sq km), Liwonde is perhaps the most popular of all of Malawis game parks, with its beautiful riverine setting and teeming wildlife offering thrilling safaris. It is about 100 miles (160 km) north of Blantyre and only rather more than half that distance from the hotels on the Mangochi Lakeshore. Wildlife viewing is enhanced because the River Shire flows along its western border, allowing boat safaris as well as the usual ones on foot or in 4x4s.</p>
                            <p class="fs-5 mb-4">Wildlife includes quite large numbers of elephants and the river attracts countless hippos and crocodiles. Lion, cheetah and wild dog have been recently reintroduced into Liwonde with the aid of African Parks who took over management of the Park in 2015 and who continue to turn Liwonde into a first class safari location. Antelope include kudu, sable and bushbuck. Leopards, hyaena and black rhino are also occasionally spotted. Birdlife is exceptionally varied. The river attracts fish eagles and weaver birds build their nests in the thin woodland. Pels fishing owl is often seen at dusk along the rivers edge.</p>
                            <p class="fs-5 mb-4">As well as the safaris and its beautiful scenery, Liwonde offers a wonderful cultural experience through visits to one of the local villages just outside the park boundary.</p>
                            <p class="fs-5 mb-4">Accommodation is first-class, with Mvuu Lodge and Kuthengo Camp both offering luxury experiences by the Shire River. Sister property to Mvuu Lodge, nearby Mvuu Camp also offers chalets as well as a camp site. To reach those lodges, visitors can approach by road and then be ferried across the river, they can drive through the park from its southern gate or they can use a pre-arranged boat from Liwonde town. There is also an airstrip near the lodges served by charter flights. New in 2020 was Chimwala Bush Camp in the southern part of the park at the base of Chinguni Hill. It offers self catering in spacious en-suite safari tents.</p>
